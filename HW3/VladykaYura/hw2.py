
user_number = input("Enter your number:")

user_number_list = list(map(int, user_number))

user_number_sum = sum(user_number_list)
print("Number sum :",user_number_sum)

user_number_sorted = sorted(user_number)

print("Number sorted :",user_number_sorted)

user_number_reverse = user_number [::-1]
print("Number reverse :",user_number_reverse)







